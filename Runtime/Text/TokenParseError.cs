namespace Cyjb.Text;

/// <summary>
/// 表示语法分析错误。
/// </summary>
public abstract class TokenParseError : IEquatable<TokenParseError>
{
	/// <summary>
	/// 获取语法分析错误的范围。
	/// </summary>
	/// <value>语法分析错误的范围。</value>
	public abstract TextSpan Span { get; }
	/// <summary>
	/// 获取语法分析错误的行列位置范围。
	/// </summary>
	/// <value>语法分析错误的行列位置范围。</value>
	public abstract LinePositionSpan LinePositionSpan { get; }

	#region IEquatable<TokenParseError> 成员

	/// <summary>
	/// 返回当前对象是否等于同一类型的另一对象。
	/// </summary>
	/// <param name="other">要比较的对象。</param>
	/// <returns>如果当前对象等于 <paramref name="other"/>，则为 <c>true</c>；否则为 <c>false</c>。</returns>
	public abstract bool Equals(TokenParseError? other);

	/// <summary>
	/// 返回当前对象是否等于另一对象。
	/// </summary>
	/// <param name="obj">要与当前对象进行比较的对象。</param>
	/// <returns>如果当前对象等于 <paramref name="obj"/>，则为 true；否则为 false。</returns>
	public override bool Equals(object? obj)
	{
		if (obj is TokenParseError other)
		{
			return Equals(other);
		}
		return false;
	}

	/// <summary>
	/// 返回当前对象的哈希值。
	/// </summary>
	/// <returns>当前对象的哈希值。</returns>
	public abstract override int GetHashCode();

	/// <summary>
	/// 返回指定的 <see cref="TokenParseError"/> 是否相等。
	/// </summary>
	/// <param name="left">要比较的第一个对象。</param>
	/// <param name="right">要比较的第二个对象。</param>
	/// <returns>如果 <paramref name="left"/> 等于 <paramref name="right"/>，则为 <c>true</c>；否则为 <c>false</c>。</returns>
	public static bool operator ==(TokenParseError? left, TokenParseError? right)
	{
		if (ReferenceEquals(left, right))
		{
			return true;
		}
		if (left is null)
		{
			return false;
		}
		return left.Equals(right);
	}

	/// <summary>
	/// 返回指定的 <see cref="TokenParseError"/> 是否不相等。
	/// </summary>
	/// <param name="left">要比较的第一个对象。</param>
	/// <param name="right">要比较的第二个对象。</param>
	/// <returns>如果 <paramref name="left"/> 等于 <paramref name="right"/>，则为 <c>true</c>；否则为 <c>false</c>。</returns>
	public static bool operator !=(TokenParseError? left, TokenParseError? right)
	{
		if (ReferenceEquals(left, right))
		{
			return false;
		}
		if (left is null)
		{
			return true;
		}
		return !left.Equals(right);
	}

	#endregion // IEquatable<TokenParseError> 成员

}
